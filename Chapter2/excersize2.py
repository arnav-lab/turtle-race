import turtle
# Create three turtles with different attributes
t1 = turtle.Turtle()
turtle.bgcolor("yellow")
t1.color("blue")
t1.penup()
t1.goto(-200, 0)
t1.pendown()
t2 = turtle.Turtle()
t2.color("red")
t2.penup()
t2.goto(0, 0)
t2.pendown()
t3 = turtle.Turtle()
t3.color("green")
t3.penup()
t3.goto(100, 0)
t3.pendown()
# Draw a square with First turtle
t1.begin_fill()
t1.forward(100)
t1.left(90)
t1.forward(100)
t1.left(90)
t1.forward(100)
t1.left(90)
t1.forward(100)
t1.left(90)
t1.end_fill()
#Draw the circle with Second Turtle
t2.begin_fill()
t2.circle(60)
t2.end_fill()
# Draw a square with Third turtle
t3.begin_fill()
t3.forward(100)
t3.left(90)
t3.forward(100)
t3.left(90)
t3.forward(100)
t3.left(90)
t3.forward(100)
t3.left(90)
t3.end_fill()
turtle.done()